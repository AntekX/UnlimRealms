#pragma once

#include "Core/ResultTypes.h"

namespace UnlimRealms
{

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Common result types
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	DEFINE_RESULT_CODE(Undefined);

	DEFINE_RESULT_CODE(Failure);

	DEFINE_RESULT_CODE(Success);

	DEFINE_RESULT_CODE(NotImplemented);

	DEFINE_RESULT_CODE(NotInitialized);

	DEFINE_RESULT_CODE(NotFound);

	DEFINE_RESULT_CODE(InvalidArgs);

	DEFINE_RESULT_CODE(OutOfMemory);


	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Result
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////	

	Result::UID Result::lastUID = -1;

	Result::Result()
	{
		Code = Undefined;
	}

	Result::Result(UID code)
		: Code(code)
	{
	}

	Result::UID Result::GenerateUID()
	{
		return lastUID++;
	}


} // end namespace UnlimRealms